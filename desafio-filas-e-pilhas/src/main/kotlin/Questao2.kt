//Interpretar expressões matemáticas não é uma tarefa trivial, por isso é comum transformamos
//expressões da forma como conhecemos (3 + 4) em uma notação polonesa reversa, que nesse caso
//teria o formato: 3 4 +. Ao fazermos isso facilita criamos um algoritmo para calcular o resultado da expressão.
//
//Para fazer tal conversão existem alguns algoritmos diferentes, um deles é chamado Shunting Yard,
//você deve implementar um conversor de notação utilizando o algoritmo de Shunting Yard simplificado,
//ou seja, ignorando a parte onde ele aceita funções no meio da expressão. Esse conversor deve utilizar
//a estrutura de dados de pilha.
//
